<?php
include connect.php;

$tsql= "INSERT INTO SalesLT.Product (Name, ProductNumber, Color, StandardCost, ListPrice, SellStartDate) VALUES (?,?,?,?,?,?);";

$params = array("BrandNewProduct", "200989", "Blue", 75, 80, "7/1/2016");
